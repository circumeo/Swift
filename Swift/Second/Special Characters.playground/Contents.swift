import Cocoa

/*
 
Exclamation Mark (!) - 참을 거짓으로, 거짓을 참으로 논리를 부정하는 연산자이며 Optional에서는 저장되어있는 값을 강제로 꺼낼때 사용
Tilde (~) - 비트연산자에서 사용
Grave Accent / Back Tick (`) - Keywords를 Identifire로 바꾸는 문법에서 사용
@ (AT Symbol) - 코드 자체에 특성을 지정하는데 사용
# (Sharp, Pound, Hashtag) - 스위프트가 제공하는 특별한 명령어들은 모두 #으로 시작
$ (Dollar Sign) - 클로저에서 패러미터 이름을 대체할때 주로 사용
% (Percent Sign) - 주로 나머지를 구할때 사용
^ (Caret) - 비트 연산에서 사용
& (Ampersand) - 주로 메모리 주소를 얻거나 참조를 전달할때 사용
* (Asterisk) - 곱하기 연산에서 사용
() (Parentheses) - 함수를 호출할때 사용, 계산의 순서를 지정할때 사용하는등 다양하게 사용
- (Minus Sign / Hyphen) - 빼기 연산에서 사용
_ (Underscore) - 와일드 카드 패턴을 구할때 자주 사용
= (Equal Sign) - 변수나 상수의 값을 저장할 때 사용, 연달아 == 으로 사용시 두개의 값을 비교
+ (Plus Sign) - 덧셈 연산에서 사용
[] (Square Bracket) - 주로 컬렉션에 저장된 값에 접근할 때 사용
{} (Curly Bracket / Brace) - 주로 코드 블록에 범위를 지정할 때 사용
\ (Backslash) - 스트링 인터폴레이션 문법이나 키패스 표현식에서 사용
| (Vertical Bar / Pipe) - 주로 논리 연산이나 비트 연산에서 사용
; (Semicolon) = 문장의 끝에 사용하나 스위프트는 자동 인식하므로 두개의 문장을 하나의 라인에서 연달아 작성할때 주로 사용
: (Colon) - 자료형을 지정할때 사용, 딕셔너리에서 키와 값을 구분할 때 사용
, (Comma) - 함수로 전달되는 값을 나열할 때 사용, 배열의 저장할 값을 나열할 때 사용
. (Period) - 메소드를 호출하거나 속성에 접근할 때 사용
<> (Angle Bracket) - 크기를 비교하는 연산자로 사용, 제네릭에서는 형식 패러미터를 지정할 때 사용
/ (Slash) - 경로를 지정할 때 사용
? (Question Mark) - 옵셔널에서 주로 사용

*/
